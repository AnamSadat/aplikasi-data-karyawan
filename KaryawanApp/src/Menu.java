    private void View(){
        list.clear();
        
        Karyawan db = new Karyawan("jdbc:mysql://localhost/", "karyawan", "root", "");
        db.setTableName("profile");
        
        for (String row: db.getKaryawan()){
            list.addElement(row);
        }
        
        ListKaryawan.setModel(list);
    }
    
