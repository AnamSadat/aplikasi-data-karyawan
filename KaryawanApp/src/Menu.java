    private void ButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonHapusActionPerformed
        // TODO add your handling code here:
        try {
            int index = ListKaryawan.getSelectedIndex();
        
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Pilih karyawan yang mau dihapus dulu.");
                return;
            }

            String row = list.getElementAt(index);
            String[] parts = row.split(" - "); 
            String id = parts[0];

            int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus ID " + id + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
            );

            if (konfirmasi == JOptionPane.YES_OPTION) {
                Karyawan db = new Karyawan("jdbc:mysql://localhost/", "karyawan", "root", "");
                db.setTableName("profile");
                db.setId(id);

                String message = db.deleteKaryawan();
                JOptionPane.showMessageDialog(this, message);

                View(); // refresh list setelah hapus
            }
        } catch (NumberFormatException  error) {
            JOptionPane.showMessageDialog(rootPane, "Database Error: " + error.getMessage());
            System.out.println("Database Error: " + error.getMessage());
        }
        
    }//GEN-LAST:event_ButtonHapusActionPerformed

